using nzy3D.Plot3D.Text.Renderers;

namespace nzy3D.Plot3D.Text.Align
{

    /// <summary>
    /// Provides constants for defining the vertical alignment of a
    /// <see cref="TextBillboardRenderer"/>, w.r.t. its position.
    /// </summary>
    /// <remarks></remarks>
    public enum Valign
    {

        /// <summary>
        /// Top vertical alignment.
        /// </summary>
        TOP,

        /// <summary>
        /// Ground vertical alignment.
        /// </summary>
        GROUND,

        /// <summary>
        /// Centered vertical alignment.
        /// </summary>
        CENTER,

        /// <summary>
        /// Bottom vertical alignment.
        /// </summary>
        BOTTOM,

        /// <summary>
        /// Default vertical alignment.
        /// </summary>
        DEFAULT

    }

}
